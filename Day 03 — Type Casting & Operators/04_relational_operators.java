/*
========================================
04_relational_operators.java
========================================
Relational Operators:

==   Equal to
!=   Not equal
>    Greater than
<    Less than
>=   Greater than or equal
<=   Less than or equal
*/

public class relational_operators {

    public static void main(String[] args) {

        int a = 10, b = 20;

        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a <= b: " + (a <= b));
    }
}