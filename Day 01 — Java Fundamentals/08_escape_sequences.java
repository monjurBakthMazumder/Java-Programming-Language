/*
========================================
Escape Sequences
========================================

\n  -> New Line
\t  -> Tab
\"  -> Double Quote
\\  -> Backslash
*/

public class escape_sequences {

    public static void main(String[] args) {

        System.out.println("Hello\nJava");

        System.out.println("Name:\tMonjur");

        System.out.println("\"Java Programming\"");

        System.out.println("C:\\Program Files\\Java");
    }
}